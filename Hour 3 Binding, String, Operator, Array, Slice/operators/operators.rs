fn main() {
    // Aritmatika
    println!("10 + 2 = {}", 10 + 2);
    println!("10 % 3 = {}", 10 % 3);

    // Logika
    println!("true AND false = {}", true && false);
    println!("NOT true = {}", !true);

    // Perbandingan
    let x = 5;
    let y = 10;
    println!("x < y: {}", x < y);
    println!("x == y: {}", x == y);
}
